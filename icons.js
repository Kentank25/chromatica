export function icon(name, classes = 'w-5 h-5') {
  const data = ICONS[name];
  if (!data) return '';
  const paths = data.d
    .map((d) => `<path stroke-linecap="round" stroke-linejoin="round" d="${d}"></path>`)
    .join('');
  return `<svg class="${classes}" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" aria-hidden="true">${paths}</svg>`;
}

export function setIcon(el, name, classes = 'w-5 h-5') {
  if (!el) return;
  el.innerHTML = icon(name, classes);
}

export function iconText(name, text, classes = 'w-5 h-5') {
  return `${icon(name, classes)}<span class="ml-1">${text}</span>`;
}
